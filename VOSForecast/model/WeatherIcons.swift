enum WeatherIcon: String {
    case ClearDay   = "\u{F00D}"
    case ClearNight = "\u{F02E}"
    case Rain       = "\u{F008}"
    case Snow       = "\u{F00A}"
    case Sleet      = "\u{F0B2}"
    case Wind       = "\u{F085}"
    case Fog        = "\u{F003}"
    case Cloudy     = "\u{F002}"
    case PartlyCloudyDay   = "\u{F07D}"
    case PartlyCloudyNight = "\u{F081}"
    case Hail         = "\u{F004}"
    case Thunderstorm = "\u{F010}"
    case Tornado      = "\u{F056}"
    case Other        = "\u{F075}"
    case None         = "\u{F095}"
}
